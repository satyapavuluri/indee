import logging

def info_log(text):
    '''
    Info log
    :param text:
    :return:
    '''
    logging.info(text)

def debug_log(text):
    '''
    Debug log
    :param text:
    :return:
    '''
    logging.debug(text)

def error_log(text):
    '''
    Error log
    :param text:
    :return:
    '''
    logging.error(text)

def warning_log(text):
    '''
    warning log
    :param text:
    :return:
    '''
    logging.warning(text)